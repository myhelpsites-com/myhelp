Sobre a história do tal <b>HTML</b>
</span>
<br>
<br>

Com o avanço da <a href="https://pt.wikipedia.org/wiki/Aplica%C3%A7%C3%A3o_web" rel="nofollow"> <b>tecnologia web</b></a>, era ideal ter uma <a href="https://pt.wikipedia.org/wiki/Linguagem_de_programa%C3%A7%C3%A3o"><b>linguagem</b></a> que pudesse ser compreendida 
por conexões distintas.

Vendo isso, <a href="https://pt.wikipedia.org/wiki/Tim_Berners-Lee" rel="nofollow"><b>Tim Berners-Lee</b></a> teve a brilhante ideia de criar o <b>HTML</b>, exatamente com essa intenção. <br>

Entretanto, só na década de 90, quando <a href="https://pt.wikipedia.org/wiki/Mosaic" rel="nofollow"><b> Mosaic — browser</b></a> desenvolvido por nada mais nada menos que 
<a href="https://pt.wikipedia.org/wiki/Marc_Andreessen"><b>Marc Andreessen</b></a> — ficou famoso, o <b>HTML</b> foi reconhecido e adotado por parte da comunidade de 
<b>desenvolvedores</b> do mundo, incluindo fabricantes de 
<a href="https://www.significados.com.br/browser/" rel="nofollow"><b>browser</b></a>, trazendo as mesmas convenções.

<b>HTML</b> significa <i>Hypertext Markup Language</i>. Mas o que isso quer dizer? 
<b>Linguagem de marcação de Hipertexto</b>. Traduzindo, uma linguagem que comporta texto, imagens, vídeos, 
áudio entre outros na <b>web</b>. Para você entender melhor, o <b>HTML</b> é baseado no princípio
 de hipertexto, que é o conjunto de <b>elementos</b> ligados por conexão, ao utilizar imagens, 
 textos, videos, etc. Ao conectá-los, formam um grande campo de informações.

<br><img src="images/blog/html5.jpg" alt=""><br>

<span style="font-size: 30px; font-weight:bolder">Agora sim, o que é esse bendito <b>HTML5</b> e como utilizar na criação de sites?
</span>
<br><br>

O <b>HTML5</b> é nada mais nada menos que a nova versão do <b>HTML</b> 4.0, a diferença entre o <b>HTML</b> e o <b>HTML5</b> é que o <b>HTML5</b> está padronizado de acordo com grupo <a href="https://www.w3schools.com/"><b>W3C</b></a>, e, além disso o <b>HTML5</b> traz funções que o <b>HTML</b> não tinha.

Por exemplo, se você precisar de criação de sites em <b>HTML5</b>, hoje podemos trabalhar com estruturas como <b>CSS</b> e o <b>Javascript</b> e fazer um trabalho mais detalhado no site, como incluir efeitos, responsividade, entre outras ferramentas para o desenvolvimento, sendo leve e super funcional.



Agora que você entendeu sobre <b>HTML</b> e <b>HTML5</b>, aposto que vai gostar também de saber mais sobre <b>CSS</b> e <b>Javascript</b>.



