                            formar o esqueleto do website">aliás, tags são utilizadas para criar componentes para
                            formar o esqueleto do <b>website</b></i>, fazendo isso temos um título salvo dentro do arquivo <b title="HTML">HTML,</b>
                        (Show!!!), atualmente existem cerca de 150 tags no <b title="HTML">HTML</b>, porém, de início vamos focar nas mais importantes:</p>
                    <ul>
                        <li title="head - define o local para colocar metadados como título, descrição e outros.">head - define o local para colocar metadados como título, descrição e outros.</li>
                        <li title="title - dentro da head o title aponta para os navegadores como deve ficar seu título.">title - dentro da head o title aponta para os navegadores como deve ficar seu título.</li>
                        <li title="body - abaixo da head ela vai guardar todas as tags que vão criar o esqueleto do website, como título que fizemos.">body - abaixo da head ela vai guardar todas as tags que vão criar o esqueleto do <b>website</b>, como título que fizemos.</li>
