                    <h2 title="O que é html?" id="html">3 - O que é html?</h2>
                    <p>
                        Um ponto muito importante é que algumas pessoas dizem que <b title="html">HTML</b> é <b title="linguagem de programação">linguagem de programação</b>,
                        isso está completamente errado, o <b title="html">HTML</b> é uma <b title="linguagem de marcação de hipertexto">linguagem de marcação de hipertexto,</b> como a
                        própria sigla diz: <b title="HyperText Markup Language"><a href="https://developer.mozilla.org/pt-BR/docs/Web/HTML" title="HyperText Markup Language" rel="nofollow">HyperText Markup Language</a></b>. Sendo assim, podemos seguir usando a
                        lógica, o <b title="HTML">HTML</b> permite que você crie conteúdo com uma estrutura simples para qualquer <b title="website">website</b>.
                        Esses hipertextos podem ser: Palavras, Imagens, Vídeos, dentre outros; formando uma espécie de rede,
                        tornando possível a comunicação entre os dados, a organização dos mesmos e até guardar informações neles.
                        Faça um teste, ao acessar um <b title="website">website</b> você vai reparar que ele trabalha de uma forma que torna perceptível ver a
                        diferença entre um título, um subtítulo ou um parágrafo. Essa organização é essencial para a leitura,
                        <b title="experiência do usuário">experiência do usuário</b> e para o SEO (Nesse artigo eu abordo um assunto magnífico). Tendo
                        o conteúdo organizado, agora é hora de aplicar o <b title="CSS e o JavaScript">CSS e o JavaScript</b>, que são essenciais para funções e
                        estilos do <b title="site">site,</b> como: cores, tamanho da fonte, ação quando clicar em um botão e etc.
                    </p>
